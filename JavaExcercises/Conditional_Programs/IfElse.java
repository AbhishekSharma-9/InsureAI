package Infosys_SpringBoard.JavaExcercises.Conditional_Programs;

public class IfElse {
    public static void main(String[] args) {
        int number = 13;

        if (number % 2 == 0) {
            System.out.println(number + " is an even number.");
        } else {
            System.out.println(number + " is an odd number.");
        }
    }
}
